import random
import math

# Generování náhodného celého čísla z rozsahu
print(random.randint(10,18))

# Generování náhodného float od 0 do 1
print(random.random())

# Generování start,stop,step - rozmezí + krok
print(random.randrange(15, 25, 2))

print(math.ceil(5.2)) # strop - vždy zokrouhlí nahoru
print(math.floor(5.2)) # zaokrouhlí vždy dolů